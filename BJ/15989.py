# f(1) -> 1 [[1]]
# f(2) -> 2 [[1,1]],[[2]]
# f(3) -> 3 [[1,1,1]],[[1,2]]=>f(1),[[3]]
# f(4) -> 4 [[1,1,1,1]],[[1,1,2],[2,2]]=>f(2),[[1,3]]=>f(1)
# f(5) -> 5 [1,1,1,1,1]],[[1,1,1,2],[1,2,2]]=>f(3),[2,3],[1,1,3]=>f(2)

tmp = [1]*10001

for i in range(2,10001):
    tmp[i] += tmp[i-2]

for i in range(3,10001):
    tmp[i] += tmp[i-3]

t = int(input())

for _ in range(t):
    print(tmp[int(input())])
